    private Order createDemoOrder() {
        Product p1 = new Product("iPhone", 1000);
        Product p2 = new Product("Laptop", 2000);

        OrderItem item1 = new OrderItem(p1, 2, 900);
        OrderItem item2 = new OrderItem(p2, 1, 1800);

        Order order = new Order();
        order.addItem(item1);
        order.addItem(item2);

        return order;
    }

