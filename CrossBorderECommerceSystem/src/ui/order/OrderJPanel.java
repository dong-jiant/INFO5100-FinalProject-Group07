    private Order getSelectedOrder() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select an order first.");
            return null;
        }
        return (Order) jTable1.getValueAt(selectedRow, 0);
    }
